# AND

#    True  and True    ==> True
#    False and True    ===> False
#    True  and False   ===> False
#    False and  Fale   ===> False

print(4 == 4  and 6 == 6)
print(4 != 4  and 6 == 6)
print(4 == 4  and 6 != 6)
print(4 != 4  and 6 != 6)


